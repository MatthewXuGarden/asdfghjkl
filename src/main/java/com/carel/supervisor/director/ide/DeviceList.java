package com.carel.supervisor.director.ide;

import java.util.*;


public class DeviceList
{
    private Map device = new HashMap();

    public DeviceList()
    {
    }

    public Device getDevice(Integer idDev)
    {
        return (Device) device.get(idDev);
    }

    public void addDevice(Integer idDev, Device dev)
    {
        device.put(idDev, dev);
    }
}
