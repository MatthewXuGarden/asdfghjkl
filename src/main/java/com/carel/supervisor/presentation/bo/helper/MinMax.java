package com.carel.supervisor.presentation.bo.helper;

public class MinMax
{
    private Integer idVarmdl = null;
    private int pos = 0;

    public MinMax(String pkValue, int pos)
    {
        idVarmdl = new Integer(pkValue.substring(2));
        this.pos = pos;
    }

    /**
     * @return Returns the idVarmdl.
     */
    public Integer getIdVarmdl()
    {
        return idVarmdl;
    }

    /**
     * @return Returns the pos.
     */
    public int getPos()
    {
        return pos;
    }
}
