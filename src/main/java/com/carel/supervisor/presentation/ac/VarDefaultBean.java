package com.carel.supervisor.presentation.ac;

public class VarDefaultBean
{
    private Integer idvar;
    private Integer index;
    private float def_value;

    public VarDefaultBean(Integer idvar, Integer index, float def_value)
    {
        this.idvar = idvar;
        this.index = index;
        this.def_value = def_value;
    }

    public float getDef_value()
    {
        return def_value;
    }

    public void setDef_value(float def_value)
    {
        this.def_value = def_value;
    }

    public Integer getIdvar()
    {
        return idvar;
    }

    public void setIdvar(Integer idvar)
    {
        this.idvar = idvar;
    }

	public Integer getIndex() {
		return index;
	}

	public void setIndex(Integer index) {
		this.index = index;
	}
    
    
}
