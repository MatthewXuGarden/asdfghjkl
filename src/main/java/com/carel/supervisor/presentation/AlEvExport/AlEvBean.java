package com.carel.supervisor.presentation.AlEvExport;

public class AlEvBean {

	private String date;
	private String descr;
	private String device;
	private String uname;
	private String endt;
	private String pr;
	
	public AlEvBean(String date,String description,String device,String username,String endtime,String priority){
		this.date=date;
		this.descr=description;
		this.device=device;
		this.uname=username;
		this.endt=endtime;
		this.pr=priority;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getDescr() {
		return descr;
	}

	public void setDescr(String descr) {
		this.descr = descr;
	}

	public String getDevice() {
		return device;
	}

	public void setDevice(String device) {
		this.device = device;
	}

	public String getUname() {
		return uname;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public String getEndt() {
		return endt;
	}

	public void setEndt(String endt) {
		this.endt = endt;
	}

	public String getPr() {
		return pr;
	}

	public void setPr(String pr) {
		this.pr = pr;
	}
	

	
	
}
