package com.carel.supervisor.presentation.menu;

public class ActionObj
{
    private String name = "";
    private String action = "";
    private String tooltip = "";
    private String iconStanby = "";
    private String iconOver = "";
    private String iconClick = "";
    private String iconDisable = "";
    
    private String description = "";
    
    public ActionObj(String name, String tooltip, String iStanby)
    {
        this.name = name;
        this.tooltip = tooltip;
        this.iconStanby = iStanby;
    }

    public String getName()
    {
        return this.name;
    }

    public String getTooltip()
    {
        return this.tooltip;
    }

    public void setAction(String action)
    {
        this.action = action;
    }

    public String getAction()
    {
        return this.action;
    }

    public String getIconStanby()
    {
        return this.iconStanby;
    }

    public String getIconClick()
    {
        return iconClick;
    }

    public void setIconClick(String iconClick)
    {
        this.iconClick = iconClick;
    }

    public String getIconDisable()
    {
        return iconDisable;
    }

    public void setIconDisable(String iconDisable)
    {
        this.iconDisable = iconDisable;
    }

    public String getIconOver()
    {
        return iconOver;
    }

    public void setIconOver(String iconOver)
    {
        this.iconOver = iconOver;
    }

	public String getDescription()
	{
		return description;
	}

	public void setDescription(String description)
	{
		this.description = description;
	}
}
