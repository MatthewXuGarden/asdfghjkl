package com.carel.supervisor.presentation.menu;

public class MenuVoce
{
    private String name = "";
    private String link = "";
    private int idVoce = 0;

    public MenuVoce()
    {
        this.name = "";
        this.link = "";
        this.idVoce = 0;
    }

    public MenuVoce(String n, String l)
    {
        this.name = n;
        this.link = l;
        this.idVoce = 0;
    }

    public MenuVoce(String n, String l, int i)
    {
        this.name = n;
        this.link = l;
        this.idVoce = i;
    }

    public String getName()
    {
        return this.name;
    }

    public String getLink()
    {
        return this.link;
    }

    public int getId()
    {
        return this.idVoce;
    }
}
