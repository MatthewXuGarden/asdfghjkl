package com.carel.supervisor.base.system;

public class DiskInfo
{
    /*Total size: 38091 (MB)
    Used: 37099 (MB)
    Free: 992 (MB)
    Serial Number: E0FC-2731
    Max of File Length Support: 255
    File System: NTFS
    */
    private long total = 0;
    private long used = 0;
    private long free = 0;
    private String serialNumber = null;
    private String fileSystem = null;
    private String driver = null;

    public DiskInfo()
    {
    }

    /**
     * @return: String
     */
    public String getFileSystem()
    {
        return fileSystem;
    }

    /**
     * @param fileSystem
     */
    public void setFileSystem(String fileSystem)
    {
        this.fileSystem = fileSystem;
    }

    /**
     * @return: long
     */
    public long getFree()
    {
        return free;
    }

    /**
     * @param free
     */
    public void setFree(long free)
    {
        this.free = free;
    }

    /**
     * @return: String
     */
    public String getSerialNumber()
    {
        return serialNumber;
    }

    /**
     * @param serialNumber
     */
    public void setSerialNumber(String serialNumber)
    {
        this.serialNumber = serialNumber;
    }

    /**
     * @return: long
     */
    public long getTotal()
    {
        return total;
    }

    /**
     * @param total
     */
    public void setTotal(long total)
    {
        this.total = total;
    }

    /**
     * @return: long
     */
    public long getUsed()
    {
        return used;
    }

    /**
     * @param used
     */
    public void setUsed(long used)
    {
        this.used = used;
    }

    /**
     * @return: String
     */
    public String getDriver()
    {
        return driver;
    }

    /**
     * @param driver
     */
    public void setDriver(String driver)
    {
        this.driver = driver;
    }
}
