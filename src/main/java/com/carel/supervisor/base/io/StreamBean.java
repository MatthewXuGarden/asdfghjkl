package com.carel.supervisor.base.io;

import java.io.*;


public class StreamBean implements Serializable
{
    private String name = null;
    private Object value = null;

    public StreamBean(String namePar, Object valuePar)
    {
        name = namePar;
        value = valuePar;
    }

    public String name()
    {
        return name;
    }

    public Object value()
    {
        return value;
    }
}
