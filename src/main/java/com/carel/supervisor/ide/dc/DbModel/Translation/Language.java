package com.carel.supervisor.ide.dc.DbModel.Translation;

import java.util.HashMap;

/**
 * @author Utente
 *
 */
public class Language {

	
	private HashMap<String, Section> sections = new HashMap<String, Section>();
	private String key;
	
	public Language()
	{
		sections = new HashMap<String, Section>();
	}

	public HashMap<String, Section> getSections() {
		return sections;
	}

	public void setSections(HashMap<String, Section> sections) {
		this.sections = sections;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}


}
