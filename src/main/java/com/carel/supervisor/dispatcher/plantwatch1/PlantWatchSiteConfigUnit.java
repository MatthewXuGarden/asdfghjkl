package com.carel.supervisor.dispatcher.plantwatch1;

public class PlantWatchSiteConfigUnit  implements Cloneable{
	int id = 0;
	String type = "";
	String description = "";
	boolean disabled = true;
	
	protected Object clone()
	{
		PlantWatchSiteConfigUnit me =new PlantWatchSiteConfigUnit();
		me.description= this.description;
		me.disabled = this.disabled;
		me.id = this.id;
		me.type = this.type;
		return me;
	}
}
