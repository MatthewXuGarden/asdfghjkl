class PlantWatchUser implements Cloneable
{
	public int Ident = 0;
	public String UserName = "";
	public String Password ="";
	
	
	protected Object clone()
	{
		PlantWatchUser me =new PlantWatchUser();
		me.Ident = this.Ident;
		me.UserName = this.UserName;
		me.Password = this.Password;
		return me;
	}
}
