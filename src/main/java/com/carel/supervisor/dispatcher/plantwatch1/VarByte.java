package com.carel.supervisor.dispatcher.plantwatch1;

public class VarByte {
	private byte val = 0;
	VarByte(byte b)
	{
		val = b;
	}
	/**
	 * @return Returns the val.
	 */
	public byte getVal() {
		return val;
	}
	/**
	 * @param val The val to set.
	 */
	public void setVal(byte val) {
		this.val = val;
	}
	
}
