package com.carel.supervisor.dataaccess.hs;

public class DataHs {

	String sql=null;
	Object [] objects=null;
	
	public DataHs(){
	}//DataHs
	
	public DataHs(String sql,Object []objects){
		this.sql=sql;
		this.objects=objects;
	}//DataHs
	
	
	public Object[] getObjects() {
		return objects;
	}//getObjects
	
	public void setObjects(Object[] objects) {
		this.objects = objects;
	}//setObjects
	
	
	public String getSql() {
		return sql;
	}//getSql

	public void setSql(String sql) {
		this.sql = sql;
	}//setSql

	
}//DataHs
